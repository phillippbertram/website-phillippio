<template>
  <header class="header-main header-transparent" :class="{'sticky': scrolled, 'navbar-style-3': 'header-style-3'}">
    <div class="container">
      <div class="row">
        <div class="col-12">
          <div class="menu-wrap">
            <div class="brand-logo">
              <a href="#">
                <img src="../assets/logo.png" alt="Brand Logo">
              </a>
            </div>
            <div id="hamburger" @click="display_menu()">
              <span />
              <span />
              <span />
            </div>
            <ul class="main-menu">
              <li v-for="item in navItems" :key="item.title">
                <nuxt-link :to="item.to">
                  {{ item.title }}
                </nuxt-link>
              </li>
            </ul>
          </div>
        </div>
      </div>
    </div>
  </header>
</template>
<script>
import navItems from '../data/navItems.json'

export default {
  data () {
    return {
      navItems,
      load: false,
      limitPosition: 200,
      scrolled: false,
      lastPosition: 500
    }
  },
  mounted () {
    window.addEventListener('scroll', this.handleScroll)
  },
  destroyed () {
    window.removeEventListener('scroll', this.handleScroll)
  },

  methods: {
    // responsive menu script
    display_menu () {
      const body = document.getElementsByTagName('body')[0];
      (!body.classList.contains('display_menu')) ? body.classList.add('display_menu') : body.classList.remove('display_menu')
    },
    close_menu () {
      const body = document.getElementsByTagName('body')[0]
      body.classList.remove('display_menu')
    },
    loaded () {
      document.getElementsByTagName('body')[0].style.overflowY = 'hidden'
      this.load = true
    },

    // sticky menu script
    handleScroll () {
      if (this.lastPosition < window.scrollY && this.limitPosition < window.scrollY) {
        this.scrolled = true
        // move up!
      }

      if (this.lastPosition > window.scrollY) {
        this.scrolled = true
        // move down
      }

      this.lastPosition = window.scrollY
      this.scrolled = window.scrollY > 50
    }
  }
}
</script>

